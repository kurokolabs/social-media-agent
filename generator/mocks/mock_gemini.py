"""Mock Gemini image client — returns a real 1x1 PNG without API calls."""

# Minimal valid 1x1 black PNG as bytes constant
MINIMAL_PNG_BYTES = bytes([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,  # IHDR chunk length + type
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,  # width=1, height=1
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,  # bit depth, color type, etc.
    0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41,  # IDAT chunk
    0x54, 0x08, 0xD7, 0x63, 0x60, 0x60, 0x60, 0x00,  # compressed pixel data
    0x00, 0x00, 0x04, 0x00, 0x01, 0x27, 0xF9, 0x40,  # CRC
    0x46, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E,  # IEND chunk
    0x44, 0xAE, 0x42, 0x60, 0x82,                    # IEND CRC
])


class MockGeminiClient:
    """Mock Gemini image client — returns a minimal valid PNG."""

    def generate_image(self, prompt: str) -> dict:
        return {
            "image_bytes": MINIMAL_PNG_BYTES,
            "prompt_used": prompt,
        }
